package application;

public class Member {

    public final String id = "";
    private final String firstname = "";
    private final String surname = "";
    private final String mail = "";
	public String getId() {
		return id;
	}
	public String getFirstname() {
		return firstname;
	}
	public String getMail() {
		return mail;
	}
	public String getSurname() {
		return surname;
	}
}
